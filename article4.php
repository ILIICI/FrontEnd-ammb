                    <p><strong>Cum calculăm bugetul necesar pentru cumpărarea unui imobil în UK?</strong></p>
                    <p>Bugetul necesar pentru cumpărarea unui imobil în UK este, cel mai des, format din câteva
                        părti:
                    <ul>
                        <li style="margin-left:2%;">Depozitul personal al cumpărătorilor care reprezintă o cotă parte din prețul total de
                            cumpărare al imobilului.
                        </li>
                        <li style="margin-left:2%;">Împrumutul ipotecar ( Mortgage).</li>
                        <li style="margin-left:2%;">Cheltuieli legate de procesul achiziționării imobilului (avocați, eventuale costuri pentru evaluarea imobilului sau expertize specializate, stamp duty și mortgage brokeri)
                            Cuantumul exact al acestor sume este absolut individual și va corespunde circumstanțelor
                            personale ale fiecărui cumpărător/debitor.
                        </li>
                    </ul>
                    </p>
                    <p><strong>Când este momentul să se aplice la mortgage?</strong></p>
                    <p>
                        Presupun că este vorba de oferta de procurare a casei. Daca este așa, atunci aplicația pentru
                        procurarea casei este depusă doar în momentul în care vânzătorul a acceptat oferta de preț
                        înaintată de către cumpărător. În acest moment practic pornește procesul de procurare al
                        imobilului. Este foarte important însă de făcut un Decision/Agreement în Principle pentru a vă asigura că sunteți eligibili pentru un credit ipotecar (mortgage). Acest act este solicitat și de către agenții imobiliari înainte de începerea negocierilor pentru vânzare/cumpărare a casei.
                    </p>
                    <p><strong>Cum securizăm cea mai bună ofertă de mortgage?</strong></p>
                    <p>
                        Consider că acest lucru poate fi făcut doar comparând produsele ipotecare existente pe piața
                        financiară din UK. Un mortgage broker este de folos aici pentru că anume cunoaşterea și
                        compararea produselor ipotecare îi și este job’ul. 
                    </p>
                    <p><strong>Ce influențează de obicei ratele și costul la un mortgage?</strong></p>
                    <p>
                        Sunt destul de mulţi factori care ar influenţa dobânzile și costul la un mortgage. Ca și exemple care afectează cel mai mult sunt: istoria creditară a cumpărătorilor, termenul creditului ipotecar (aici sunt importante vârsta, ocupația clienților și tipul de mortgage solicitat), cuantumul depozitului achitat de către cumpărător (cu cât este mai mare % depozitului, dobânda la mortgage va scădea), posibile scheme de procurare a casei (Shared Ownership,Help To Buy Equty Loan etc).
                    </p>
                    <p><strong>Cel fel de investigații cere banca pentru a da împrumutul, mă refer la surveys</strong></p>
                    <p>
                        Banca va împuternici un evaluator, achitat uneori de către cumpărători, pentru efectuarea
                        unui Survey Standard, pentru scopul doar de mortgage, al casei. În cazul când evaluatorul va
                        depista unele detalii la imobil ce ar putea face ineligibilă casa pentru mortgage, atunci fie pot
                        fi solicitate nişte rapoarte specializate (în dependenţă de detaliul depistat de către surveyor),
                        fie banca ar putea refuza acordarea creditului pentru procurarea acestui imobil.
                    </p>
                    <p><strong>Cum alegem proprietatea: leasehold or freehold? Cum influențează asupra împrumutului?</strong></p>
                    <p>
                        Consider, la acest capitol, că fiecare persoana își alege casa pe care o dorește și și-o poate
                        permite. Leasehold sau freehold reprezintă drepturile pe care îl are proprietarul asupra
                        imobilului. În cazul Freehold – este un drept de proprietate absolut și gratuit asupra imobilului.
                        În cazul proprietăţilor Leasehold – cumpărătorul are un drept doar de folosință asupra
                        imobilului pentru o periodă de timp. De foarte multe ori – contra unei plaţi anuale.
                        Este foarte important ca un cumpărător să fie informat din start de către vânzători sau agenții
                        imobiliari dacă proprietatea este Leasehold sau Freehold, iar în cazul în care este Leasehold –
                        care este durata rămasă a Lease’ului. Asta pentru că deținerea unei proprietăți Leasehold
                        presupune foarte des și o plată anuala (Ground Rent), la fel ca și anumte limitări de modificare sau utilizare a imobilului.
                    </p>
                    <p>
                        Băncile au și ele criterii specifice pentru proprietățile Leasehold, cele mai importante
                        referindu-se la perioada rămasă a Lease’ului. Astfel că ar putea să nu fie acceptate proprietăți
                        cu un Lease mai mic de 30 de ani la finalul termenului de mortgage. FOARTE IMPORTANT!
                        Termenul de 30 de ani este doar INDICATIV, băncile având criterii diferite și nu neapărat 30 de ani! Înaintea începerii negocierilor cu vânzătorii, perioada rămasă pe Lease trebuie
                        coordonata cu banca de unde intenționați să accesați creditul ipotecat sau, eventual, cu
                        mortgage brokerul care vă reprezintă.
                    </p>
