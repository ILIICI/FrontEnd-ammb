                            calcula cuantumul acestei taxe în dependenţă de valoarea tranzacției, informația referitoare
                            la deținerea în trecut sau prezent a unor alte imobile.
                        </dd>
                        <dt><strong>•	Durata procesului</strong></dt>
                        <dd style="margin: 0;padding: 0 0 0.9em 0;">Este foarte individual, depinde, și aici, de o sumedenie de factori: termenul în care banca
                            examinează cererea de creditare ipotecară, verificările pe care le fac avocații, posibilitatea
                            vânzătorului de a elibera casa, a cumpărătorului de a face completion și, desigur, pandemia.
